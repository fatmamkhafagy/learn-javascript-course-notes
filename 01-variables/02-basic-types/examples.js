// Example 1: Boolean
var isHappy = true;
var isSleeping = false;
console.log(typeof isHappy);      // 'boolean'
console.log(typeof isSleeping);   // 'boolean'

// Example 2: String
var favoriteColor = "Blue";
var userName = "TMTM";
console.log(typeof favoriteColor); // 'string'
console.log(typeof userName);      // 'string'

// Example 3: Number
var age = 25;
var temperature = 36.6;
console.log(typeof age);         // 'number'
console.log(typeof temperature); // 'number'

// Example 4: Undefined
var futurePlan;
console.log(typeof futurePlan);  // 'undefined'

// Mixed Example
var hasAccess = false;           // boolean
var personName = "Layla";        // string
var points = 100;                // number
var unknown;                     // "undefined"

console.log(typeof hasAccess);   // 'boolean'
console.log(typeof personName);  // 'string'
console.log(typeof points);      // 'number'
console.log(typeof unknown);     // 'undefined'
